
package sexercisek;

public class UseOneLoop {
    
    public static void main(String[] args) {
        
        for (int i = 10; i >= 1; i--) {
            System.out.print(i+" ");
        }
        
        for (int j = 2; j <= 10; j++) {
            System.out.print(j+" ");
        }
        
    }  
}
